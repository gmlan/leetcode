package leetcodehelper;
import java.util.*;

public class PrintHelper {
	public static void Print(List<String> list){
		for(String sz : list)
			System.out.print(sz + " ");
		System.out.println("");
		 
	}
	
	public static void PrintInt(List<Integer> list){
		for(Integer sz : list)
			System.out.print(sz + " ");
		System.out.println("");
		 
	}
}
